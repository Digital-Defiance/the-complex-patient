const { withDangerousMod } = require('@expo/config-plugins');
const fs = require('fs');
const os = require('os');
const path = require('path');

/** Gradle 9 removed IBM_SEMERU; @react-native/gradle-plugin still pins foojay 0.5.0. */
const GRADLE_DISTRIBUTION =
  'distributionUrl=https\\://services.gradle.org/distributions/gradle-8.13-bin.zip';

function resolveAndroidSdkDir() {
  const fromEnv = process.env.ANDROID_HOME || process.env.ANDROID_SDK_ROOT;
  if (fromEnv && fs.existsSync(fromEnv)) {
    return fromEnv;
  }

  const candidates = [
    path.join(os.homedir(), 'Library/Android/sdk'),
    path.join(os.homedir(), 'Android/Sdk'),
  ];
  if (process.env.LOCALAPPDATA) {
    candidates.push(path.join(process.env.LOCALAPPDATA, 'Android/Sdk'));
  }

  return candidates.find((candidate) => fs.existsSync(candidate)) ?? null;
}

function formatSdkDirForLocalProperties(sdkDir) {
  return sdkDir.replace(/\\/g, '\\\\');
}

async function ensureLocalProperties(androidRoot) {
  const localPropertiesPath = path.join(androidRoot, 'local.properties');
  if (fs.existsSync(localPropertiesPath)) {
    const existing = await fs.promises.readFile(localPropertiesPath, 'utf8');
    if (/^\s*sdk\.dir\s*=/m.test(existing)) {
      return;
    }
  }

  const sdkDir = resolveAndroidSdkDir();
  if (!sdkDir) {
    return;
  }

  const sdkDirLine = `sdk.dir=${formatSdkDirForLocalProperties(sdkDir)}`;
  const contents = fs.existsSync(localPropertiesPath)
    ? `${await fs.promises.readFile(localPropertiesPath, 'utf8').then((text) => text.trimEnd())}\n${sdkDirLine}\n`
    : `# Generated by with-android-gradle8.js — do not commit.\n${sdkDirLine}\n`;

  await fs.promises.writeFile(localPropertiesPath, contents);
}

/**
 * Android local-build fixes:
 * - Pin Gradle wrapper to 8.x (foojay 0.5.0 vs Gradle 9 incompatibility)
 * - Write local.properties with sdk.dir when ANDROID_HOME is unset (EAS local temp dirs)
 */
function withAndroidGradle8(config) {
  return withDangerousMod(config, [
    'android',
    async (config) => {
      const androidRoot = config.modRequest.platformProjectRoot;

      const wrapperPath = path.join(androidRoot, 'gradle/wrapper/gradle-wrapper.properties');
      const contents = await fs.promises.readFile(wrapperPath, 'utf8');
      const updated = contents.replace(/distributionUrl=.*/m, GRADLE_DISTRIBUTION);
      await fs.promises.writeFile(wrapperPath, updated);

      await ensureLocalProperties(androidRoot);

      return config;
    },
  ]);
}

module.exports = withAndroidGradle8;
